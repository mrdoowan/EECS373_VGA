	 // video structure constants 
	 parameter hpixels = 800;    // horizontal pixels per line 
	 parameter vlines = 525;     // vertical lines per frame 
	 parameter hpulse = 96;    // hsync pulse length 
	 parameter vpulse = 2;     // vsync pulse length 
	 parameter hbp = 144;         // end of horizontal back porch 
	 parameter hfp = 784;         // beginning of horizontal front porch 
	 parameter vbp = 31;       // end of vertical back porch 
	 parameter vfp = 511;         // beginning of vertical front porch 
	 // active horizontal video is therefore: 784 - 144 = 640 
	 // active vertical video is therefore: 511 - 31 = 480 
		
	 // Storing the horizontal & vertical counters 
	 reg [9:0] hc; 
	 reg [9:0] vc; 

	 // Horizontal & vertical counters -- 
	 // this is how we keep track of where we are on the screen 
	 always @(posedge VGA_CLK) 
	 begin 
		  // reset condition 
		  if (reset) 
		  begin 
				hc <= 0; 
				vc <= 0; 
		  end 
		  else 
		  begin 
				// keep counting until the end of the line 
				if (hc < hpixels - 1) 
					 hc <= hc + 1; 
				else 
				// When we hit the end of the line, reset the horizontal 
				// counter and increment the vertical counter. 
				// If vertical counter is at the end of the frame, then 
				// reset that one too. 
				begin 
					 hc <= 0; 
					 if (vc < vlines - 1) 
						  vc <= vc + 1; 
					 else 
						  vc <= 0; 
				end 
				 
		  end 
	 end 
		
	 // generate sync pulses (active low) based on the  
	  // Horizontal and Vertical Counter 
	 assign hsync = (hc < hpulse) ? 0:1; 
	 assign vsync = (vc < vpulse) ? 0:1; 
		
	 always @* 
	 begin 
		  //-------------- 
		  // ROWS 0 - 60 
		  //-------------- 
		  if (vc >= vbp && vc < (vbp+60)) 
		  begin 
				if (hc >= hbp && hc < (hbp+315)) 
				begin 
					 red = 255; 
					 green = 255; 
					 blue = 255; 
				end 
				else if (hc >= (hbp+315) && hc < (hbp+325))
				begin
					 red = 0;
					 green = 0;
					 blue = 0;
				end
				else if (hc >= (hbp+325) && hc < (hbp+640))
				begin
					 red = 255;
					 green = 255;
					 blue = 255;
				end
				// Outside horizontal range, display black 
				else 
				begin 
					 red = 0; 
					 green = 0; 
					 blue = 0; 
				end 
		  end 
